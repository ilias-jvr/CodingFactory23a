package gr.aueb.cf.ch3;

/**
 * Εκτυπώνει 10 αστεράκια (10 φορές από
 * ένα αστεράκι).
 *
 * @author liakos (28/03/2023)
 */
public class Stars10App {

    public static void main(String[] args) {

        int i = 1;

        while (i <= 10) {
            System.out.print("* ");
            i++;
        }
    }
}
